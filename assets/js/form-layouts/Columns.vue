<template>
    <div v-bind="$_layoutWrapperAttributes" v-show="!$_hidden">
        <div v-for="(col,index) in components"
            :key="index"
            :class="colClass(col)">
            <component v-bind="$_attributes(col)" />
        </div>
    </div>
</template>

<script>
import Layout from '../mixins/Layout'

export default {
    mixins: [Layout],
    computed:{
        $_customClassArray(){
            return [
                'row',
                this.$_data('alignClass'),
                this.$_data('guttersClass')
            ]
        },
        numOfColumns(){
            return this.component.components.length
        },
        equalColumnClass(){
            return 12 % this.numOfColumns == 0 ? 
                'col-'+this.breakpoint+parseInt(12/this.numOfColumns) : 'col'
        },
        breakpoint(){
            return this.$_data('breakpoint') ? this.$_data('breakpoint')+'-' : ''
        }
    },
    methods:{
        colClass(col){
            return this.$_data('col', col) || this.equalColumnClass
        }
    }
}
</script>
